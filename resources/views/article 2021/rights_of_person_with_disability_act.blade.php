            <div class="address-icon" >
                <i class="fa fa-circle text-dark"></i>
            </div>
            <h4>Principles stated to be implemented for empowerment of persons with disabilities (PWD) are respect for inherent dignity, individual autonomy including the freedom to make one's own choices, and independence of persons.</h4>
        </div>
        
