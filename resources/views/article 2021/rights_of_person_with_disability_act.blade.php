            <h4>After India signed and ratified the United Nations Convention on the Rights of Persons with Disabilities (UNCRPD) in 2007, the process of enacting a new legislation in place of the Persons with Disabilities Act, 1995 (PWD Act, 1995) began in 2010 to make it compliant with the UNCRPD.</h4>
