            <div class="address-icon" >
                <i class="fa fa-circle text-dark"></i>
            </div>
            <h4>The RPWD Act, 2016 provides that “the appropriate Government shall ensure that the PWD enjoy the right to equality, life with dignity, and respect for his or her own integrity equally with others.”</h4>
