        </div>
        <div class="sigle-address ml-5" aria-hidden="true">
            <div class="address-icon" >
                <i class="fa fa-circle text-dark"></i>
            </div>
            <h4>In order to protect them from alienation, the (Equal Rights, Protection of Rights and Full Participation Act) came into force in 1996 this Act covered only 7 types of disability as it was seen it was lacking short of today’s realities the new act tries to cover it.</h4>
        </div>
        <div class="sigle-address ml-5" aria-hidden="true">
            <div class="address-icon" >
                <i class="fa fa-circle text-dark"></i>
            </div>
            <h4>In the RPWD Act, 2016, the list has been expanded from 7 to 21 conditions and it now also includes cerebral palsy, dwarfism, muscular dystrophy, acid attack victims, hard of hearing, speech and language disability, specific learning disabilities, autism spectrum disorders, chronic neurological disorders such as multiple sclerosis and Parkinson's disease, blood disorders such as haemophilia, thalassemia, and sickle cell anaemia, and multiple disabilities.</h4>
        </div>
        
        <div class="sigle-address ml-5" aria-hidden="true">
            <div class="address-icon" >
                <i class="fa fa-circle text-dark"></i>
            </div>
            <h4>The nomenclature mental retardation is replaced by intellectual disability which is defined as “a condition characterized by significant limitation both in intellectual functioning (reasoning, learning, problem-solving) and in adaptive behavior which covers a range of every day social and practical skills including specific learning disabilities and autism spectrum disorders.”</h4>
        </div>
        <div class="sigle-address ml-5" aria-hidden="true">
            <div class="address-icon" >
                <i class="fa fa-circle text-dark"></i>
            </div>
            <h4>The Act provides an elaborate definition of mental illness which is “a substantial disorder of thinking, mood, perception, orientation, or memory that grossly impairs judgment, behavior, and capacity to recognize reality or ability to meet the ordinary demands of life but does not include retardation which is a condition of arrested or incomplete development of mind of a person, especially characterized by subnormality of intelligence.”</h4>
        </div>
