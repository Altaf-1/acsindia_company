Government of India had initiated to examine policies related to old age income security in 
India, the government decided to replace the then defined benefit pension system to defined 
contribution pension system with respect to all new joinees of Central/State Government except 
armed forces.</h4>
        </div>
        <div class="sigle-address ml-5" aria-hidden="true">
            <div class="address-icon" >
                <i class="fa fa-circle text-dark"></i>
            </div>
            <h4>Accordingly, the Interim pension fund regulatory and development Authority (IPRDA) bill was 
passed by Union parliament in February 2003, which established IPRDA to promote, develop, 
and regulate pension system in India.
</h4>
        </div>
        <div class="sigle-address ml-5" aria-hidden="true">
            <div class="address-icon" >
                <i class="fa fa-circle text-dark"></i>
            </div>
            <h4>And the final system i.e., the Pension Fund Regulatory and Development Authority (PFRDA) 
was established with the President’s assent on 19 September 2013 and was made a permanent 
Act. </h4>
        </div>
        
        <div class="sigle-address ml-5" aria-hidden="true">
            <div class="address-icon" >
                <i class="fa fa-circle text-dark"></i>
            </div>
            <h4>The PFRDA is a statutory regulatory body set up under PFRDA Act with an objective to 
promote old age income security and protect the interests of National Pension Scheme 
subscribers.</h4>
        </div>
        <div class="sigle-address ml-5" aria-hidden="true">
            <div class="address-icon" >
                <i class="fa fa-circle text-dark"></i>
            </div>
            <h4>Objective/Vision: To be a model Regulator for promotion and development of an organized 
pension system to serve the old age income needs of people on a sustainable basis.</h4>
        </div>
        <div class="sigle-address ml-5" aria-hidden="true">
            <div class="address-icon" >
                <i class="fa fa-circle text-dark"></i>
            </div>
            <h4>While the pension system was initially introduced for Government employees, it is now 
extended to all citizens of India and non-resident Indians including self-employed persons.</h4>
        </div>
        
        <div class="sigle-address pt-2" aria-hidden="true">
            <div class="address-icon" >
                <i class="fa fa-star text-dark ml-3"></i>
            </div>
