        </div>
        <div class="sigle-address ml-5" aria-hidden="true">
            <div class="address-icon" >
                <i class="fa fa-circle text-dark"></i>
            </div>
            <h4>The principle reflects a paradigm shift in thinking about disability from a social welfare concern to a human rights issue.</h4>
        </div>
        
