            <div class="address-icon" >
                <i class="fa fa-circle text-dark"></i>
            </div>
            <h4>The pension regulator, which oversees the National Pension System (NPS) with retirement 
savings of ₹6.76 lakh crore, is working with the Insurance Regulatory Development Authority of 
India (IRDAI) to provide such an option to its more than 4.7 crore members that include 
government and private sector employees as well as self-employed and informal sector workers.
</h4>
        </div>
        <div class="sigle-address pt-2" aria-hidden="true">
            <div class="address-icon" >
                <i class="fa fa-star text-dark ml-3"></i>
            </div>
            <h3><strong>National Pension Scheme (NPS)
</strong></h3>
        </div>
        <div class="sigle-address ml-5" aria-hidden="true">
            <div class="address-icon" >
                <i class="fa fa-circle text-dark"></i>
            </div>
            <h4>National Pension System (NPS) is a voluntary, defined contribution retirement savings scheme 
designed to enable the subscribers to make optimum decisions regarding their future through 
systematic savings during their working life.
</h4>
        </div>
        <div class="sigle-address ml-5" aria-hidden="true">
            <div class="address-icon" >
                <i class="fa fa-circle text-dark"></i>
            </div>
            <h4>NPS seeks to inculcate the habit of saving for retirement amongst the citizens. 
</h4>
        </div>
        <div class="sigle-address ml-5" aria-hidden="true">
            <div class="address-icon" >
                <i class="fa fa-circle text-dark"></i>
            </div>
            <h4>It is an attempt towards finding a sustainable solution to the problem of providing adequate 
retirement income to every citizen of India.
</h4>
        </div>
        <div class="sigle-address ml-5" aria-hidden="true">
            <div class="address-icon" >
                <i class="fa fa-circle text-dark"></i>
            </div>
            <h4>It is introduced by PFRDA whereby subscribers’ contributions are collected and accumulated in 
an individual pension account using various intermediaries. 
</h4>
        </div>
        <div class="sigle-address ml-5" aria-hidden="true">
            <div class="address-icon" >
                <i class="fa fa-circle text-dark"></i>
            </div>
            <h4>Under NPS, individual contributions are pooled together into a pension fund and are invested as 
per approved investment guidelines.
</h4>
        </div>
        <div class="sigle-address ml-5" aria-hidden="true">
            <div class="address-icon" >
                <i class="fa fa-circle text-dark"></i>
            </div>
            <h4>Funds are generally invested in diversified portfolios consisting of government bonds, bills, 
corporate debentures, and shares, based on subscribers’ choice. 
</h4>
        </div>
        <div class="sigle-address ml-5" aria-hidden="true">
            <div class="address-icon" >
                <i class="fa fa-circle text-dark"></i>
            </div>
            <h4>Subscribers also have an option, at the time of exit, to purchase a life annuity by using 
accumulated pension fund.
</h4>
        </div>
        <div class="sigle-address ml-5" aria-hidden="true">
            <div class="address-icon" >
                <i class="fa fa-circle text-dark"></i>
            </div>
